def get_resources_for_skill(skill: str) -> list:
    """Returns 1-2 resources for a given skill. Falls back to default."""
    # Exact match (case-insensitive)
    key = next((k for k in RESOURCE_MAP if k.lower() == skill.lower()), None)
    # Partial match — check if skill is contained in any key
    if not key:
        key = next((k for k in RESOURCE_MAP if skill.lower() in k.lower() or k.lower() in skill.lower()), None)
    if not key:
        key = "__default__"

    resources = RESOURCE_MAP[key]
    # Fill in skill name in default URLs
    return [
        {**r, "url": r["url"].replace("{skill}", skill.replace(" ", "+"))}
        for r in resources[:2]
    ]


def enrich_roadmap_with_resources(roadmap: dict, missing_skills: list) -> dict:
    """
    Attaches resources to each phase action.
    Matches action text against missing_skills to find relevant resource.
    Falls back to default if no match.
    """
    for phase_key in ["phase_1", "phase_2", "phase_3"]:
        phase = roadmap.get(phase_key, {})
        enriched_actions = []
        for action in phase.get("actions", []):
            # Handle case where action is already a dict (enriched)
            action_text = action if isinstance(action, str) else action.get("action", "")
            # Find which missing skill this action is about
            matched_skill = next(
                (skill for skill in missing_skills if skill.lower() in action_text.lower()),
                None,
            )
            # If no missing skill matched, try matching against common keywords in the action
            if not matched_skill:
                for word in action_text.split():
                    clean = word.strip(".,;:()\"'").lower()
                    if len(clean) > 3 and clean in {k.lower() for k in RESOURCE_MAP if k != "__default__"}:
                        matched_skill = clean
                        break
            resources = get_resources_for_skill(matched_skill or "__default__")
            enriched_actions.append({
                "action": action_text,
                "resources": resources,
            })
        phase["actions"] = enriched_actions
    return roadmap
